public class Seigyo {
    public static void main(String[] args) {
        int x = 0;
        int y = 1;
        if (x == y) {
            System.out.println("xとyは等しい");
        } else if (x > y) {
            System.out.println("xはyより大きい");
        } else {
            System.out.println("xはyより小さい");
        }
    }
}

    