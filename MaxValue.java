public class MaxValue {
    public static void main(String[] args) {
        // Java の基本型の最小値と最大値を出力する
        System.out.println("byte   最小値: " + Byte.MIN_VALUE);
        System.out.println("byte   最大値: " + Byte.MAX_VALUE);
        System.out.println("short  最小値: " + Short.MIN_VALUE);
        System.out.println("short  最大値: " + Short.MAX_VALUE);
        System.out.println("int    最小値: " + Integer.MIN_VALUE);
        System.out.println("int    最大値: " + Integer.MAX_VALUE);
        System.out.println("long   最小値: " + Long.MIN_VALUE);
        System.out.println("long   最大値: " + Long.MAX_VALUE);
        System.out.println("float  最小値: " + Float.MIN_VALUE);
        System.out.println("float  最大値: " + Float.MAX_VALUE);
        System.out.println("double 最小値: " + Double.MIN_VALUE);
        System.out.println("double 最大値: " + Double.MAX_VALUE);

        // byte   最小値: -128
        // byte   最大値: 127
        // short  最小値: -32768
        // short  最大値: 32767
        // int    最小値: -2147483648
        // int    最大値: 2147483647
        // long   最小値: -9223372036854775808
        // long   最大値: 9223372036854775807
        // float  最小値: 1.4E-45
        // float  最大値: 3.4028235E38
        // double 最小値: 4.9E-324
        // double 最大値: 1.7976931348623157E308
    }
}